PROMPT Creando tabla CLIENTE...;
PROMPT ------------------------------------------------------------------------;
CREATE TABLE CLIENTE (
	DNI			CHAR(10),
	Nombre		VARCHAR2(25),
	Apellidos	VARCHAR2(25),
	Direccion	VARCHAR2(50),
	Localidad	VARCHAR2(15),
	Provincia	VARCHAR2(15),
	PRIMARY KEY(DNI)
);


PROMPT Creando tabla TARIFA...;
PROMPT ------------------------------------------------------------------------;
CREATE TABLE TARIFA (
	IDT					NUMBER(2),
	Nombre				VARCHAR2(25),
	Tipo				CHAR(1),
	Establecimiento		NUMBER(3,2),
	Manana				NUMBER(3,2),
	Tarde				NUMBER(3,2),
	Reduccion_Interna	NUMBER(3,1),
	PRIMARY KEY (IDT)
);


PROMPT Creando tabla LINEA...;
PROMPT ------------------------------------------------------------------------;
CREATE TABLE LINEA (
	Numero				CHAR(9),
	DNI					CHAR(10),
	Fecha_Adscripcion	DATE,
	IDT					NUMBER(2),
	Ultimo_Cambio		DATE,
	PRIMARY KEY (Numero),
	FOREIGN KEY (DNI) REFERENCES CLIENTE(DNI),
	FOREIGN KEY (IDT) REFERENCES TARIFA(IDT)
);


PROMPT Creando tabla CONTRATO...;
PROMPT ------------------------------------------------------------------------;
-- Formato de Cuenta Corriente: 'ENTIDAD(4)-SUCURSAL(4)-CONTROL(2)-NUMEROCUENTA(10)'
CREATE TABLE CONTRATO (
	Numero				CHAR(9),
	Fecha_Alta			DATE,
	Cuenta_Corriente	VARCHAR2(23),
	PRIMARY KEY (Numero),
	FOREIGN KEY (Numero) REFERENCES LINEA(Numero)
);


PROMPT Creando tabla PREPAGO...;
PROMPT ------------------------------------------------------------------------;
CREATE TABLE PREPAGO (
	Numero					CHAR(9),
	Fecha_Ultima_Recarga	DATE,
	Saldo_Actual			NUMBER(5,2),
	PRIMARY KEY (Numero),
	FOREIGN KEY (Numero) REFERENCES LINEA(Numero)
);


PROMPT Creando tabla LLAMADA...;
PROMPT ------------------------------------------------------------------------;
CREATE TABLE LLAMADA (
	Origen		CHAR(9),
	Destino		CHAR(9),
	Fecha		DATE,
	Hora		INTERVAL DAY TO SECOND,
	Duracion	INTERVAL DAY TO SECOND,
	PRIMARY KEY (Origen, Destino, Fecha, Hora),
	FOREIGN KEY (Origen) REFERENCES LINEA(Numero)
);



